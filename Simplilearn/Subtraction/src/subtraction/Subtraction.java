package subtraction;

public class Subtraction 
{

	public static void main (String [] args) 
	{
		int a = 20, b = 16;
		//subtraction
		System.out.println("Subtraction result is = " + (a-b));
		
		//Division
		
		System.out.println("Division of Two number = "+ (a/b));
		
		//Multiplication
		System.out.println("Multiplication of two numbers is = "+ (a*b));
		
		//Modulos operator
		
		System.out.println("Modulos of two numbers = "+ (a%b));
	}
}
